<script>
import { defineComponent, reactive, onMounted, onUnmounted } from 'vue'
export default defineComponent({
  name: 'FlowLine',
  setup () {
    const state = reactive({ width: document.body.clientWidth })

    function resize() {
      state.width = document.body.clientWidth
      console.log(state.width)
    }

    onMounted(() => {
      const container = document.getElementById('FlowGroup')
      const pathLength = container.querySelectorAll('path')
      pathLength.forEach((item) => {
        const itemLength = Math.ceil(item.getTotalLength())
        item.setAttribute('stroke-dasharray', itemLength)
        if (item.parentNode.parentNode.className.baseVal.includes('blue')) {
          item.setAttribute('stroke-dashoffset', -itemLength)
        } else {
          item.setAttribute('stroke-dashoffset', itemLength)
        }
      })

      window.addEventListener('resize', resize, false)
    })

    onUnmounted(() => {
      window.removeEventListener('resize', resize, false)
    })
    return {}
  }
})
</script>

